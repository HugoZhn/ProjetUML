
public class Parametres {
	float coefCorrectifV;
	float coefCorrectifC;
	
	public Parametres(float cV, float cC) {
		this.coefCorrectifC = cC;
		this.coefCorrectifV = cV;
	}

	public float getCoefCorrectifV() {
		return coefCorrectifV;
	}

	public float getCoefCorrectifC() {
		return coefCorrectifC;
	}
	
}
